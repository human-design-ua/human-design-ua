// ── Single Claude API call ────────────────────────────────────
async function callClaude(systemPrompt, userPrompt, maxTokens = 8000) {
  // Large outputs (8000+ tokens) can take many minutes — stream to avoid
  // non-streaming request timeouts that cut the response short mid-JSON.
  const stream = client.messages.stream({
    model: 'claude-sonnet-4-6',
    max_tokens: maxTokens,
    system: systemPrompt,
    messages: [{ role: 'user', content: userPrompt }],
  });
  if (process.env.HD_DEBUG_STREAM) {
    const t0 = Date.now();
    let n = 0;
    stream.on('text', () => { n++; if (n % 20 === 0) console.log(`  [stream ${((Date.now()-t0)/1000).toFixed(1)}s] chunks: ${n}`); });
    stream.on('error', (e) => console.log('  [stream error]', e.message));
  }
  const msg = await stream.finalMessage();
  const raw = msg.content[0].text.trim()
    .replace(/^```json\s*/i, '').replace(/^```\s*/i, '').replace(/```\s*$/i, '').trim();
  try {
    return JSON.parse(raw);
  } catch(e) {
    const fixed = tryFixJson(raw);
    if (fixed) return fixed;
    throw new Error('JSON parse failed: ' + e.message + ' | stop_reason: ' + msg.stop_reason + ' | len: ' + raw.length + ' | end: ' + raw.slice(-200));
  }
}

function tryFixJson(str) {
  try {
    const lastObj = str.lastIndexOf('"}');
    const lastArr = str.lastIndexOf('"]');
    const cutAt = Math.max(lastObj, lastArr);
    if (cutAt > 200) {
      const attempt = str.slice(0, cutAt + 2);
      // Close any open arrays/objects
      const opens = (attempt.match(/\[/g)||[]).length - (attempt.match(/\]/g)||[]).length;
      const openObj = (attempt.match(/\{/g)||[]).length - (attempt.match(/\}/g)||[]).length;
      const suffix = ']'.repeat(Math.max(0,opens)) + '}'.repeat(Math.max(0,openObj));
      return JSON.parse(attempt + suffix);
    }
  } catch(e) {}
  return null;
}

